export default {
  name: 'IndexContentDownloadClient',
  data(){
    return {
      store,
      getPageEnter: ''
    }
  },
  created(){

  },
  mounted(){
    var _this = this;
    let pageEnterStatus = sessionStorage.getItem('DLS_pageEnter');
    setTimeout(function(){
      _this.getPageEnter = pageEnterStatus;
    }, 600)
    _this.pageScrollBar();
  },
  components: {
    NetCloudMusicFooter
  },
  methods: {
    pageScrollBar() {
      let _this = this;
      window.addEventListener('scroll', function(){
        let scrollTop = document.documentElement.scrollTop;
        console.log(scrollTop)
        if(scrollTop < 500 && scrollTop > 100 || scrollTop == 100 ){
          $(_this.$refs.svwtH).css({
            transition: 'all 1s ease',
            transform: 'rotateY(0deg) translateZ(0px) translateX(0px)',
            opacity: 1
          })
          $(_this.$refs.svwImg).css({
            transition: 'transform 1s ease',
            transform: 'rotateX(0deg)'
          })
          $(_this.$refs.svwtP).css({
            transition: 'opacity 2s ease 1s',
            opacity: 1
          })
        }else if(scrollTop > 500 || scrollTop == 500){
          $(_this.$refs.tvwtP).css({
            transition: 'all 1s ease',
            transform: 'translateY(0px)',
            opacity: 1
          })
        }
      })
    }
  },
  computed: {

  },
  watch: {
    getPageEnter(newValue, oldValue){
      // console.log(newValue)
      if(newValue){
        // console.log(this.$refs.viewWrapImg);// JSDOM
        // console.log(this.$refs.versionShow)
          $(this.$refs.viewWrapImg).css({
            transform: 'translate(812px, 0)',
            transition: 'transform 1s ease'
          })
          $(this.$refs.versionShow).css({
            transform: 'translate(-638px, 0)',
            transition: 'transform 1s ease'
          });
        this.$store.commit('updateDownloadLoadStatus', 0)
        }
      }
    }
  }
</script>

